                                    // User inputs and output Examples                                                                                                       

// Scanner: A class in java.util package used for obtaining input of primitive types.

import java.util.Scanner; //

public class InputExample {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.print("Enter your name: ");
        String name = scanner.nextLine();
        System.out.println("Hello, " + name + "!");
    }
}


// System.out: Used to print output to the console.

public class OutputExample {
    public static void main(String[] args) {
        System.out.println("This is an output example.");
    }
}

